import java.util.*;
public class q15 {
        public static void main(String[] args)
        {

            Scanner sc = new Scanner(System.in);

            System.out.print("Enter the number of terms: ");
            int n = sc.nextInt();

            for (int i = 1; i <= n; i++)
            {

                System.out.print(i * i + " ");
            }
        }
    }
